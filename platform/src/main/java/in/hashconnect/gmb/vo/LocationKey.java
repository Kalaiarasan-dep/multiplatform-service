package in.hashconnect.gmb.vo;

public class LocationKey {

	private String plusPageId;
	private String placeId;
	private String explicitNoPlaceId;
	private String requestId;

	public String getPlusPageId() {
		return plusPageId;
	}

	public void setPlusPageId(String plusPageId) {
		this.plusPageId = plusPageId;
	}

	public String getPlaceId() {
		return placeId;
	}

	public void setPlaceId(String placeId) {
		this.placeId = placeId;
	}

	public String getExplicitNoPlaceId() {
		return explicitNoPlaceId;
	}

	public void setExplicitNoPlaceId(String explicitNoPlaceId) {
		this.explicitNoPlaceId = explicitNoPlaceId;
	}

	public String getRequestId() {
		return requestId;
	}

	public void setRequestId(String requestId) {
		this.requestId = requestId;
	}
}
